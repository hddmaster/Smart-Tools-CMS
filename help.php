<h1>Инструменты</h1>

  <h3>Статистика</h3>
    <p class="margin_left">Подробная статистика посещаемости сайта показывает отчеты как по одному дню, так и
    по выбранному периоду времени. Каждый день, при наличии посетителей на сайте, в 24:00 происходит агрегация данных
    статистики и запись в архив.</p>

    <p class="margin_left">В основном окне системы управления отображается динамика посетителей сайта за сегодня. Уникальность посетителя определяется
    строго по IP-адресу, т.е. уникальный посетитель сегодня - это уникальный IP-адрес. Система статистики формирует отчеты по ссылающимся
    страницам, поисковым словам, популярности страниц, распределению хитов (просмотров страниц) по IP-адресам, строкам user-agent.</p>
    
    <p class="margin_left">Для просмотра полного списка отчета необходимо нажать на ссылку "подробнее". Для просмотра информации
    об IP-адресе на ссылку справа от адреса "whois".</p>

    <p class="margin_left">Для просмотра статистики по выбранному дню нужно выбрать нужный день в форме, указать фильтр отчета и
    нажать клавишу "Показать".</p>

    <p class="margin_left">Для просмотра статистики по выбранному периоду нужно выбрать нужный промежуток времени в форме, указать фильтр отчета и
    нажать клавишу "Показать".</p>

