  <h3>Рассылки</h3>
    <p class="margin_left">В основном окне выводится список рассылок с пиктограммами управления. Для редактирования рассылки и
    выбора сообщения для рассылки необходимо нажать <img src="/admin/images/icons/pencil.png" alt="" border="0" align="absmiddle"> в колонке
    "Сообщения". Для выбора подписчиков на рассылку необходимо нажать <img src="/admin/images/icons/pencil.png" alt="" border="0" align="absmiddle">
    в колонке "Подписчики". В редакторе подписчиков можно синхронизировать список подписчиков со списком активированных
    пользователей. После внесения изменений необходимо нажать на клавишу "Сохранить".</p>

    <p class="margin_left">Для управления видимостью на сайте воспользуйтесь клавишами <img src="/admin/images/icons/light-bulb.png" alt="" border="0" align="absmiddle"> и
    <img src="/admin/images/icons/light-bulb-off.png" alt="" border="0" align="absmiddle">. Если рассылка неактивная, клавиша отправить блокируется.</p>

    <p class="margin_left">Для того чтобы добавить рассылку необходимо заполнить обязательные поля соответствующей формы.
    После того, как сверху списка появится рассылка, можно приступить к выбору подписчиков и сообщения, нажав
    <img src="/admin/images/icons/pencil.png" alt="" border="0" align="absmiddle"> справа от названия рассылки в соответствующих колонках.</p>

  <h3>Сообщения</h3>
    <p class="margin_left">В основном окне выводится список сообщений с пиктограммами управления. Для редактирования сообщения
    необходимо нажать <img src="/admin/images/icons/pencil.png" alt="" border="0" align="absmiddle">. После этого откроется
    редактор в новом окне. После внесения изменений необходимо нажать на клавишу "Сохранить".</p>

    <p class="margin_left">Для управления видимостью на сайте воспользуйтесь клавишами <img src="/admin/images/icons/light-bulb.png" alt="" border="0" align="absmiddle"> и
    <img src="/admin/images/icons/light-bulb-off.png" alt="" border="0" align="absmiddle">. Если сообщение неактивное, его нельзя отправить, оно
    автоматически удаляется из рассылки.</p>

    <p class="margin_left">Для того чтобы добавить сообщение необходимо заполнить обязательные поля соответствующей формы.
    После того, как сверху списка появится сообщение, можно приступить к редактированию текстовых данных, нажав на кнопку
    <img src="/admin/images/icons/edit.png" alt="" border="0" align="absmiddle"> справа от заголовка сообщения.</p>
    
    <p class="margin_left">Файл, вложенный в сообщение не отправляются вместе с сообщением. Он хранится на сайте, а в сообщениях появляется
    ссылка на файл. Если сообщение неактивно, ссылка на файл продолжает работать и файл остается доступным для скачивания.</p>



